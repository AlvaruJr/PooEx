
class Empresa {
    private String razaoSocial;
    private String cnpj;
    private String fone;

    public void setRazaoSocial(String rs) {
        this.razaoSocial = rs;
    }

    public void setCNPJ(String c) {
        this.cnpj = c;
    }

    public void setFone(String f) {
        this.fone = f;
    }

    public String getRazaoSocial() {
        return razaoSocial;
    }

    public String getCNPJ() {
        return cnpj;
    }

    public String getFone() {
        return fone;
    }
}
